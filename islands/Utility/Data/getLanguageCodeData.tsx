export const getLanguageCodeDatabase = (): languageCodeDataRes[] => {
    return database;
};

export const findLanguageCodeDataFromCode = (code: string): languageCodeDataRes => {
    return (
        database.find((data) => data.ISO_639_1 === code || data.ISO_639_2_T === code || data.ISO_639_2_B === code || data.ISO_639_3 === code) ||
        UnknownLanguageCodeData
    );
};

export const findLanguageCodeDataFromName = (name: string): languageCodeDataRes => {
    return database.find((data) => data.name === name) || UnknownLanguageCodeData;
};
